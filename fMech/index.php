<h2>What kind of jobs include fluid mechanics?</h2>
<div class="secSep"></div>
<p>Aerospace engineering requires a good understanding of fluid properties, as air and water both act in very similar ways. I include water in aerospace since boats nowadays fly through the water. Engineers that build dams and buildings need to understand some basic fluid mechanics to predict how sewage and water will interact with their design.</p>
